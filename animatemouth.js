function handleMouseMove(e) {
	e.preventDefault();
	e.stopPropagation();
	var canvasOffset = $canvas.offset();
	var offsetX = canvasOffset.left;
	var offsetY = canvasOffset.top;
	var mouseX = parseInt(e.pageX - offsetX);
	var mouseY = parseInt(e.pageY - offsetY);

	var cx = canvas.width * 0.11;
	var cy = canvas.height * 0.45;
	var dx = mouseX - cx;
	var dy = mouseY - cy;
	var isInside = dx * dx + dy * dy <= circle.radius * circle.radius;

	if (isInside && !circle.wasInside) {
		drawMouth(circle, isInside);
	} else if (!isInside && circle.wasInside) {
		drawMouth(circle, isInside);
	}
}

$("#canvas").mousemove(function (e) {
	handleMouseMove(e);
});
